    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    try{
		String usuario = jTextField1.getText();
		String pass = jPasswordField1.getText();

		if("".equals(usuario) || "".equals(pass)){
			JOptionPane.showMessageDialog(null, "no se puede dejar ningún campo vacío");
		} else {
			boolean usuarioEnLista = LU.Buscar(usuario);

			if(usuarioEnLista){
				if(this.LU.verificarContrasenia(usuario, pass) == true){
					principal p = new principal(this.LU);
					p.setVisible(true);
					dispose();
				} else {
					JOptionPane.showMessageDialog(null, "El nombre o contraseña no es correcto.");
				}
			} else {
				JOptionPane.showMessageDialog(null, "El usuario no existe");
			}
		}
	} catch(Exception e){
            JOptionPane.showMessageDialog(null, "No se ha podido ingresar");
	}
    }//GEN-LAST:event_jButton1ActionPerformed

